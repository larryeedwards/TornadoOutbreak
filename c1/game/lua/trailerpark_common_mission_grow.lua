function on_label(label)

  if label == "cinematic_start" then
    gomgr.getbyoid(487).path = gomgr.getbyoid(257)
    gomgr.getbyoid(488):dispatchlabel("cine_fly")
    gomgr.getbyoid(377).trackedobject = gomgr.getbyoid(487)
    gomgr.getbyoid(378).trackedobject = gomgr.getbyoid(488)
    gomgr.getbyoid(234).curphase = 1
    gomgr.getbyoid(233).curphase = 1
    gomgr.getbyoid(227):dispatchlabel("weatherphase0")
    if realcine == false then
      cameramgr.screenfade(0, 0.0, 0.1, 1.5)
      gomgr.getbyoid(195):settext("textTransitionPrompt", 918)
      gomgr.getbyoid(195):settext("textZone", 900)
      gomgr.getbyoid(195):start()
    end
    for i = 1, #cine_firesprites do
      cine_firesprites[i]:dispatchlabel("reset")
    end

  elseif label == "cinematic_split" then
    if modulecompleted == false or realcine == false then
      gomgr.getbyoid(487).path = gomgr.getbyoid(256)
    end
    
  elseif label == "cinematic_shadow" then
    if modulecompleted == false or realcine == false then
      gomgr.getbyoid(374):dispatchlabel("grow")
      gomgr.getbyoid(227):dispatchlabel("grow")
      gomgr.getbyoid(227).curphase = 2
      gomgr.getbyoid(270):addtoworld()
      finishnaturally = true
    end
    
  elseif label == "cinematic_avatar_at_end" then
    if modulecompleted == false or realcine == false then
     gomgr.getbyoid(504):dispatchlabel("loopingidle")
    end
    
  end
  
end

function checkforcomplete()
  if realcine == true and modulecompleted == false then
    pause(1.0)
    beginwait()
      gomgr.getbyoid(195):settext("textTransitionPrompt", 918)
      gomgr.getbyoid(195):settext("textZone", 900)
      gomgr.getbyoid(195):start()
    endwait()
    cameramgr.screenfade(0, 0.7, 0.25, 1.0)
    
    if finishnaturally == false then
      for i = 1, #cine_firesprites do
        cine_firesprites[i]:dispatchlabel("remove")
      end
    end
    
    pause(0.8)
    
    modulecompleted = true
    realcine = false
    requestcomplete(true)
    else

  end
end

